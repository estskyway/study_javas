public class Arrays {
    public static void main(String[] args) {
        String[] cars = {"Volvo", "BMW", "Ford", "Mazda"};
        
        for (int first = 0; first < 4; first = first + 1) {
            System.out.println(cars[first]);
        }
        System.out.println("end");
        // return 0;
    }
}
